package com.model_class;

public class Patients {
	private int patient_id;
	private int patient_ssn_id;
	private String patient_name;
	private int patient_age;
	private String date_of_admission;
	private String type_of_bed;
	private String patient_address;
	private String patient_state;
	private String patient_city;
	private String patient_status;
	
	public int getPatient_id() {
		return patient_id;
	}
	public void setPatient_id(int patient_id) {
		this.patient_id = patient_id;
	}
	public int getPatient_ssn_id() {
		return patient_ssn_id;
	}
	public void setPatient_ssn_id(int patient_ssn_id) {
		this.patient_ssn_id = patient_ssn_id;
	}
	public String getPatient_name() {
		return patient_name;
	}
	public void setPatient_name(String patient_name) {
		this.patient_name = patient_name;
	}
	public int getPatient_age() {
		return patient_age;
	}
	public void setPatient_age(int patient_age) {
		this.patient_age = patient_age;
	}
	public String getDate_of_admission() {
		return date_of_admission;
	}
	public void setDate_of_admission(String date_of_admission) {
		this.date_of_admission = date_of_admission;
	}
	public String getType_of_bed() {
		return type_of_bed;
	}
	public void setType_of_bed(String type_of_bed) {
		this.type_of_bed = type_of_bed;
	}
	public String getPatient_address() {
		return patient_address;
	}
	public void setPatient_address(String patient_address) {
		this.patient_address = patient_address;
	}
	public String getPatient_state() {
		return patient_state;
	}
	public void setPatient_state(String patient_state) {
		this.patient_state = patient_state;
	}
	public String getPatient_city() {
		return patient_city;
	}
	public void setPatient_city(String patient_city) {
		this.patient_city = patient_city;
	}
	public String getPatient_status() {
		return patient_status;
	}
	public void setPatient_status(String patient_status) {
		this.patient_status = patient_status;
	}
	
}
